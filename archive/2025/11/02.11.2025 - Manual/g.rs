#[allow(unused)]
use algo_lib::dbg;
use algo_lib::io::input::Input;
use algo_lib::io::output::Output;

fn solve(input: &mut Input, out: &mut Output) {
    let tc = input.usize();
    for _ in 0..tc {
        let n = input.i64();
        let k = input.i64();
        let mut m = input.i64();
        let max_div = n / k;
        let pairs = (max_div - 1) / 2;
        let mut res = 1 + pairs;
        let free = n - 1 - pairs * 2;
        let ones = free.min(m);
        res += ones;
        m -= ones;
        if m > 0 {
            let tmp = (m / 2).min(pairs);
            res += tmp;
        }
        out.println(res);
    }
}

pub(crate) fn run(mut input: Input, mut output: Output) -> bool {
    solve(&mut input, &mut output);
    output.flush();
    true
}

#[cfg(feature = "local")]
